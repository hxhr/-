#define N_POW	112		//幂的次数
#define N_NUM	N_POW+1	//十进制形式的小数位数

/***************************************************************************
  函数名称：
  功    能：输出某一行的幂次（某个一维数组的内容）
  输入参数：
  返 回 值：
  说    明：pormpt为非空串则输出串内容
				  为空串则输出“2^-xxx”，具体见demo
***************************************************************************/
void output(const char prompt[], const char d[])
{
	if (prompt == NULL) {

		cout << "2^" << setiosflags(ios::right) << setw(4) << (-1) * (int)d[0] << " : 0.";

		for (int i = 1; i < d[0] + 1; i++) {
			cout << d[i];
		}
		cout << endl;
		//字符串的输出，直接写数组名,但二维数组怎么输出单行？
		//其实不是怎么输出字符串的问题，还有就是demo中其实也不是输出一行的字符串，例如11行则输出到11格，
		//所以我现在考虑用循环来输出


	}
	else {
		cout << prompt;

		for (int i = 0; d[i] != 0; i++) {
			cout << d[i];
		}
		cout << endl;
	}
}

/***************************************************************************
  函数名称：
  功    能：用二维数组第i行的值，计算第i+1行
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
void calc_next(char d_cur[], char d_next[])//char类型的0-9怎么算？48-57，但其实可以全当int，输出时加0就行
{
	int point = 0;
	for (int i = 1; i < 113; i++) {
		d_next[i] = (d_cur[i] + point) / 2;
		if (d_cur[i] % 2 != 0) {
			point = 10;
		}
		else {
			point = 0;//必须重置。因为这句话卡了很久
		}
	}
}

/***************************************************************************
  函数名称：
  功    能：根据幂次表，计算某个二进制小数的十进制值
  输入参数：char pure_decimal[] : 存放二进制小数的数组
			char out[] ：存放计算得到的十进制小数
		   char power_table[][N_NUM + 1]：幂次表
  返 回 值：
  说    明：
 ***************************************************************************/
void calc_num(char pure_decimal[], char out[], char power_table[][N_NUM + 1])
{
	for (int i = 1; i < 113; i++) {//存放二进制小数的,ij从1开始是因为powertable方便看
		if (pure_decimal[i - 1] == '1') {
			for (int j = 1; j < 113; j++) {//out数组要初始化为0
				out[j - 1] = power_table[i][j] - '0' + out[j - 1];
			}
			for (int k = 111; k >= 0; k--) {//char数组改成int
				while (out[k] >= 10) {
					out[k] = out[k] - 10;
					out[k - 1] = out[k - 1] + 1;
				}
			}
		}
	}
}

